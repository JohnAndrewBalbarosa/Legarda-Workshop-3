// Merge selectors derived from behavior_log.jsonl (via scripts/extract-profiles.mjs).
// The file only exists after the extraction script has run — missing file is not an error.
// signin-choice intentionally keeps selectors:[] because first-match semantics mean
// any log entry on the signin URL matches signin-choice (listed first in PROFILES),
// so no derived selectors accumulate for it.
try {
  const derivedPath = fileURLToPath(new URL('./derived-profiles.json', import.meta.url));
  const derivedMap = JSON.parse(readFileSync(derivedPath, 'utf8'));
  for (const profile of PROFILES) {
    const derived = derivedMap[profile.id] ?? [];
    const highConf = derived.filter((d) => d.count >= 2).map((d) => d.selector);
    if (highConf.length > 0) {
      profile.selectors = [...new Set([...highConf, ...profile.selectors])];
    }
  }
} catch { /* derived-profiles.json absent — use hardcoded selectors only */ }

function findProfileForUrl(url) {
  if (typeof url !== 'string') return null;
  return PROFILES.find((p) => {
    try { return p.match(url); } catch { return false; }
  }) ?? null;
}

export function getProfileById(profileId) {
  return PROFILES.find((p) => p.id === profileId) ?? null;
}

export function getHighlightsForUrl(url, { signinChoice = null } = {}) {
  const profile = findProfileForUrl(url);
  if (!profile) return { id: 'none', label: 'No match', selectors: [] };

  // signin-choice landing: render no highlights until the user picks one of three paths.
  if (profile.id === 'signin-choice') {
    if (signinChoice) {
      const targetId =
        signinChoice === 'root' ? 'signin-root-existing' :
        signinChoice === 'iam'  ? 'signin-root-iam' :
        signinChoice === 'new'  ? 'signup-root-new' : null;
      if (targetId) {
        const target = getProfileById(targetId);
        if (target) return { id: target.id, label: target.label, selectors: target.selectors.slice() };
      }
    }
    return { id: profile.id, label: profile.label, selectors: [] };
  }

  // Profiles gated on a choice — only emit selectors when the gate matches.
  if (Array.isArray(profile.gateOnChoice) && !profile.gateOnChoice.includes(signinChoice)) {
    return { id: profile.id, label: profile.label, selectors: [] };
  }

  return { id: profile.id, label: profile.label, selectors: profile.selectors.slice() };
}

// Ordered sequence of all profiles in the expected navigation flow.
// Profiles marked transitional:true sit in the sign-in corridor between
// aws-home and console-home — the exact path varies per user (OAuth redirect,
// password, MFA, etc.) so they are treated as "in transit" rather than wrong.
export const NAVIGATION_SEQUENCE = [
  { id: 'aws-home',             transitional: false },
  { id: 'signin-choice',        transitional: true  },
  { id: 'signin-root-existing', transitional: true  },
  { id: 'signin-root-iam',      transitional: true  },
  { id: 'signup-root-new',      transitional: true  },
  { id: 'signin-password',      transitional: true  },
  { id: 'signin-mfa',           transitional: true  },
  { id: 'console-home',         transitional: false },
  { id: 'ec2-dashboard',        transitional: false },
  { id: 'ec2-launch-form',      transitional: false },
];

const TRANSITIONAL_IDS = new Set(
  NAVIGATION_SEQUENCE.filter((n) => n.transitional).map((n) => n.id)
);

// Compare the participant's current page URL with the step's expected profile.
// Returns 'correct' | 'wrong' | 'unknown'.
//   - 'correct'  → the URL matches the profile the step expects
//   - 'wrong'    → the URL is clearly at a different non-transitional step
//   - 'unknown'  → URL is unrecognized, or URL is in the sign-in corridor
//                  (transitional) while the step expects a post-signin profile
export function expectedUrlMatcher(expectedProfile, url) {
  if (!expectedProfile) return 'unknown';
  const profile = findProfileForUrl(url);
  if (!profile) return 'unknown';
  if (profile.id === expectedProfile) return 'correct';
  if (TRANSITIONAL_IDS.has(profile.id) && !TRANSITIONAL_IDS.has(expectedProfile)) return 'unknown';
  return 'wrong';
}

// Code that runs INSIDE the page (passed to frame.evaluate as a string).
// Kept self-contained — no closures, no imports.
export const FRAME_INJECTION_FN = function applyHighlights(selectors) {
  if (!document.body) return 0;

  if (!document.getElementById('w-style')) {
    const s = document.createElement('style');
    s.id = 'w-style';
    s.textContent = `
      @keyframes w-ripple {
        0%   { outline: 2px solid rgba(220,38,38,0.95); outline-offset: 0px;  box-shadow: 0 0 0 0   rgba(220,38,38,0.7); }
        55%  { outline: 3px solid rgba(220,38,38,1);    outline-offset: 4px;  box-shadow: 0 0 0 10px rgba(220,38,38,0.25); }
        100% { outline: 4px solid rgba(220,38,38,0.85); outline-offset: 8px;  box-shadow: 0 0 0 20px rgba(220,38,38,0); }
      }
      [data-w-hl] {
        animation: w-ripple 0.85s infinite ease-out !important;
        border-radius: 6px !important;
        position: relative !important;
        z-index: 2147483646 !important;
      }
    `;
    (document.head || document.documentElement).appendChild(s);
  }

  const isVisible = (el) => {
    if (!el || !el.getBoundingClientRect) return false;
    const r = el.getBoundingClientRect();
    if (r.width < 4 || r.height < 4) return false;
    const cs = window.getComputedStyle(el);
    if (cs.display === 'none' || cs.visibility === 'hidden' || cs.opacity === '0') return false;
    return true;
  };

  const matchSelector = (sel) => {
    if (typeof sel !== 'string' || !sel) return [];
    if (sel.startsWith('text=')) {
      const needle = sel.slice(5).trim().toLowerCase();
      if (!needle) return [];
      const out = [];
      const candidates = document.querySelectorAll(
        'button, a, span, label, div[role="button"], input[type="submit"], input[type="button"], [role="link"], h1, h2, h3'
      );
      candidates.forEach((el) => {
        const t = (el.innerText || el.value || el.textContent || '').trim().toLowerCase();
        if (t && t.includes(needle) && t.length < 200) out.push(el);
      });
      return out;
    }
    if (sel.startsWith('aria=')) {
      const needle = sel.slice(5).trim().toLowerCase();
      if (!needle) return [];
      return Array.from(document.querySelectorAll('[aria-label]')).filter((el) => {
        const v = (el.getAttribute('aria-label') || '').toLowerCase();
        return v.includes(needle);
      });
    }
    try { return Array.from(document.querySelectorAll(sel)); } catch { return []; }
  };

  const apply = () => {
    document.querySelectorAll('[data-w-hl]').forEach((el) => el.removeAttribute('data-w-hl'));
    if (!Array.isArray(selectors) || selectors.length === 0) return 0;
    let hit = 0;
    for (const sel of selectors) {
      const matches = matchSelector(sel).filter(isVisible);
      if (matches.length > 0) {
        // First-match-wins per selector — avoids highlighting the entire page.
        matches.slice(0, 3).forEach((el) => {
          el.setAttribute('data-w-hl', 'true');
          hit++;
        });
        // Stop after the first selector that produced any hits — gives stable
        // single-target blink instead of every selector firing at once.
        if (hit > 0) break;
      }
    }
    // Vertically center the first highlighted element (smooth, only if not
    // already roughly centered to avoid scroll jitter).
    const first = document.querySelector('[data-w-hl]');
    if (first) {
      const rect = first.getBoundingClientRect();
      const vh = window.innerHeight || document.documentElement.clientHeight;
      const center = rect.top + rect.height / 2;
      const offFromCenter = Math.abs(center - vh / 2);
      if (offFromCenter > vh * 0.25) {
        try { first.scrollIntoView({ block: 'center', behavior: 'smooth' }); } catch {}
      }
    }
    return hit;
  };

  window.__wSelectors = selectors;
  if (window.__wObserver) {
    try { window.__wObserver.disconnect(); } catch {}
  }
  window.__wObserver = new MutationObserver(() => {
    // Re-apply only if the highlighted nodes were removed/replaced.
    const live = document.querySelectorAll('[data-w-hl]').length;
    if (live === 0) apply();
  });
  window.__wObserver.observe(document.body, { childList: true, subtree: true });
  return apply();
};

export async function injectBlinkInAllFrames(page, selectors) {
  const fnSource = FRAME_INJECTION_FN.toString();
  const frames = page.frames();
  const results = await Promise.all(
    frames.map(async (frame) => {
      try {
        const hits = await frame.evaluate(`(${fnSource})(${JSON.stringify(selectors)})`);
        return { url: frame.url(), hits: hits || 0 };
      } catch {
        return { url: frame.url(), hits: 0 };
      }
    })
  );
  return results;
}
