// Highlight Engine
// Maps the current URL (and a small amount of DOM context) to a list of
// selectors that should blink. Used by aws-guide-playwright.mjs.
//
// Each profile has:
//   id:        stable identifier referenced from WorkshopPlan.expectedProfile
//   match:     (url) => boolean
//   label:     short human-readable label for logs
//   selectors: array of selector strings. Supports:
//     - normal CSS selectors
//     - "text=foo"  (case-insensitive substring match against innerText/value)
//     - "aria=foo"  (matches aria-label / aria-labelledby substring)
//   gateOnChoice (optional): selectors are only applied when injectionState.signinChoice
//     equals one of the listed values. Used so the AWS sign-in landing page does
//     NOT blink upfront — the user must pick "existing" or "new" first.
//
// Derived selectors are merged from user/derived-profiles.json when that file
// exists (generated by scripts/extract-profiles.mjs from behavior_log.jsonl).
// They are prepended so real observed selectors are tried before the hardcoded
// fallbacks.

